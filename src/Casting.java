public class Casting {

    public static void main(String[] args) {
        System.out.println("First Exercise");
        double data1 = 112.35;
        int data2 = (int) data1;
        System.out.println(data1);
        System.out.println(data2);

        System.out.println("\nSecond Exercise");
        String variable1 = "41";
        int variable2 = Integer.parseInt(variable1);
        System.out.println("The string value is: "+variable1);
        System.out.println("The integer value is: "+variable2);

    }
}
